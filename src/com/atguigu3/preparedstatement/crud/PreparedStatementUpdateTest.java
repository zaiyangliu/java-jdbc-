			String sql = "insert into employees(first_name, last_name, email)values(?,?,?)"; //占位符
			ps =  conn.prepareStatement(sql);
			//5.填充占位符
			ps.setString(1, "lu");
			ps.setString(2, "ang");
			ps.setString(3, "ang@afs.com");
